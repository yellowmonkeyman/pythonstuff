fave_music = [
    "Incubus - Drive",
    "Led Zeppelin - Stairway to Heaven",
    "21 Pilots - Heathens"
]

print(fave_music)

#list.append will add an item to the end of a list

fave_music.append("Black Sabbath - War Pigs")

print("APPEND - add on to the end of a list")
print(fave_music) 

print("POP - remove last in a list")
fave_music.pop()

print(fave_music)